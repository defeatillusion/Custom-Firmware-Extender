extern void clearRect(u32 color)
{
	Point pen;
	for(pen.x = p0.x; pen.x <= p1.x; pen.x++)
	{
		for(pen.y = p0.y; pen.y <= p1.y; pen.y++)
		{
			clearPixel(&pen);
			if(color) putPixelColor(color, &pen);
		}
	}
}

static enum DrawCharError drawChar(char cht, Point * pen, bool flag, bool ignore)
{
	if(pen->x + 8 > p1.x && !ignore)
	{
		return ENDLINE;
	}
	if(pen->y > p1.y && !ignore)
	{
		return ENDPAGE;
	}
	if(cht == 0x0A || cht == 0x0D)
	{
		return NEWLINE;
	}

	u8 ch = cht - 0x20;

	Point tpen;
	u8 g[2];
	int wid, i, j, c, d;
	u8 gray;

	wid = font[fontp[ch] + 1] / 2 + ((font[fontp[ch] + 1] % 2)? 1: 0);
	for(i = 0; i < font[fontp[ch]]; i++)
	{
		for(j = 0; j < font[fontp[ch] + 1]; j+=2)
		{
			gray = font[fontp[ch] + 4 + i * wid + j/2];
			g[0] = (gray & 0xF0) >> 4;
			g[1] = (gray & 0x0F);
			for(c = 0; c < 2 && (j + c) < font[fontp[ch] + 1]; c++)
			{
				if(g[c] > 0)
				{
					tpen.x = pen->x + font[fontp[ch] + 3] + j + c;
					tpen.y = pen->y - font[fontp[ch] + 2] + i;
					putPixelColor(((((u8)(g[c] * 17) * A(Gcolor[1])/255) << 24) | BGR(Gcolor[1])), &tpen);
					for(d = 3; d > 0 && flag; d --)
					{
						tpen.x ++;
						tpen.y ++;
						putPixelColor(((((u8)(g[c] * d) * A(Gcolor[3])/255) << 24) | BGR(Gcolor[3])), &tpen);
					}
				}
			}
		}
	}
	pen->x += 8;
	return DRAWED;
}

extern int drawString_p(const char * txt, u16 flag, Point * pt0)
{
	Point pen;
	enum DrawCharError error;
	pen.x = pt0->x;
	pen.y = pt0->y;
	int n = 0, nl = 0;
	bool shadow = false;
	bool endline = flag & FENDLINE;
	if(flag & FSHADOW)
	{
		shadow = true;
	}
	while(txt[n] > 0)
	{
		error = drawChar(txt[n], &pen, shadow, !endline);
		if(flag & FTHICK)
		{
			pen.x -= 7;
			error = drawChar(txt[n], &pen, shadow, true);
		}
		if(error == ENDLINE || error == NEWLINE)
		{
			nl ++;
			pen.x = pt0->x;
			pen.y = pt0->y + line_height * nl;
			if(error == NEWLINE)
			{
				n ++;
			}
		}
		else if(error == ENDPAGE)
		{
			return n;
		}
		else
		{
			n ++;
		}
	}
	return -1;
}

extern int drawString(const char * txt, u16 flag)
{
	return drawString_p(txt, flag, &p0);
}

static void initblur()
{
	Point pen;
	int sumr = 0,
		sumg = 0,
		sumb = 0;
	int i, j, k;
	u32 color = 0;
	int width = SCREEN_WIDTH,
		height = SCREEN_HEIGHT;
	int offset;
	r = (u8 *)sceKernelAllocHeapMemory(heapid, width * height * sizeof(u8));
	g = (u8 *)sceKernelAllocHeapMemory(heapid, width * height * sizeof(u8));
	b = (u8 *)sceKernelAllocHeapMemory(heapid, width * height * sizeof(u8));
	for(i = 1 ; i < SCREEN_WIDTH - 2; i ++)
	{
		for(j = 1; j < SCREEN_HEIGHT - 2; j ++)
		{
	    	sumr = sumg = sumb = 0;
	    	for(k = 0; k < gauss_width; k ++)
			{
				setXY(&pen, i - ((gauss_width - 1) >> 1) + k, j);
				getPixelColor(&pen, &color);
	      		sumr += R(color) * gauss_fact[k];
	      		sumg += G(color) * gauss_fact[k];
	      		sumb += B(color) * gauss_fact[k];
	    	}
			offset = width * j + i;
			r[offset] = sumr / gauss_sum;
			g[offset] = sumg / gauss_sum;
			b[offset] = sumb / gauss_sum;
		}
	}
}

extern void blur()
{
	Point pen;
	for(pen.x = p0.x ; pen.x < p1.x; pen.x ++)
	{
		for(pen.y = p0.y; pen.y < p1.y; pen.y ++)
		{
			blurPixel(&pen);
		}
	}
}


